#include <libft.h>

char	*ft_strcat(char *s1, const char *s2)
{
	char	*p;

	p = s1;
	while (*s1)
		s1++;
	s1 = ft_strcpy(s1, s2);
	while (*s1)
		s1++;
	*s1 = '\0';
	return (p);
}
