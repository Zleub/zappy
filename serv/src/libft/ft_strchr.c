#include <libft.h>

char	*ft_strchr(const char *s, int c)
{
	if (c == '\0')
	{
		while (*s)
			s++;
		return (char*)(s + ft_strlen(s));
	}
	while (*s)
		if (*s != (unsigned char)c)
			s++;
		else
			return (char*)(s);
	return (NULL);
}
